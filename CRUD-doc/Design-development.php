	   <p style="font-family: 'Libre Baskerville', serif;" id="f">Design describes the visual appearance of a website. Traditional approach involves contrast, coloring, balance, emphasis, rhythm, style of graphical elements (lines, shapes, texture, color and direction), use of icons, background textures and general atmosphere of overall website design. Design is the process of collecting ideas, and aesthetically arranging and implementing them, guided by certain principles for a specific purpose. Web design is a similar process of creation, with the intention of presenting the content on electronic web pages, which the end-users can access through the internet with the help of a web browser.<br>
