        <strong style="font-size: 22px;color: black;font-family: 'Grenze Gotisch', cursive;"><u>Web Page Anatomy</u></strong><br>
        <p style="font-family: 'Libre Baskerville', serif;">A web site includes the following components-</p><br>
        <label class="font-weight-bold text-muted">Containing Block</label><br>
        <p style="font-family: 'Libre Baskerville', serif;"><strong>Container</strong> can be in the form of page’s body tag, an all containing div tag. Without container there would be no place to put the contents of a web page.</p><br>
        <label class="font-weight-bold text-muted">Logo</label><br>
        <p style="font-family: 'Libre Baskerville', serif;"><strong>Logo</strong> refers to the identity of a website and is used across a company’s various forms of marketing such as business cards, letterhead, brouchers and so on.</p><br>
        <label class="font-weight-bold text-muted">Navigation</label><br>
        <p style="font-family: 'Libre Baskerville', serif;">The site’s <strong>navigation system</strong> should be easy to find and use. Oftenly the anvigation is placed rigth at the top of the page.</p><br>
        <label class="font-weight-bold text-muted">Content</label><br>
        <p style="font-family: 'Libre Baskerville', serif;">The content on a web site should be relevant to the purpose of the web site.</p><br>
        <label class="font-weight-bold text-muted">Footer</label><br>
        <p style="font-family: 'Libre Baskerville', serif;"><strong>Footer</strong> is located at the bottom of the page. It usually contains copyright, contract and legal information as well as few links to the main sections of the site.</p><br>
        <label class="font-weight-bold text-muted">Whitespace/nagative space</label><br>
        <p style="font-family: 'Libre Baskerville', serif;">It is also called as negative space and refers to any area of page that is not covered.</p><br>
           
