	     	<strong style="font-size: 22px;color: black;font-family: 'Grenze Gotisch', cursive;"><u>Back-end developer</u></strong><br>
	     	<p class="px-3 py-2" style="font-family: 'Libre Baskerville', serif;">These developers are experts in programming and using complex software languages like Java, SQL and C#. The work they do is not seen by users because it involves coding on the web servers and databases, not on the browser (unlike front-end developers, which you’ll soon find out).<br>
	     	In short, their job is to create logical, functional websites using various programmes. They also spend a lot of time testing and fixing bugs.</p>
	     	<strong style="font-size: 22px;color: black;font-family: 'Grenze Gotisch', cursive;"><u>Front-end developer</u></strong><br>
	     	<p class="px-3 py-2" style="font-family: 'Libre Baskerville', serif;"><strong>This is also known as client-side development.</strong> It’s more connected to web design than back-end development because it involves using HTML, CSS, and JavaScript to create things the user can see, like client lead forms.<br>
	     	In summary, like a back-end developer, those developing the front-end have coding skills but they build frameworks the user can actually interact with.</p>
	     	<strong style="font-size: 22px;color: black;font-family: 'Grenze Gotisch', cursive;"><u>Full-stack developer</u></strong><br>
	     	<p class="px-3 py-2" style="font-family: 'Libre Baskerville', serif;"><strong>This is also known as client-side development.</strong> A full-stack developer works across different layers – or stacks – including both the front and back end. <strong>It is essentially the full package when it comes to web development, because they are an expert in all areas.</strong> </p>
